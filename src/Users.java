    private void jButton_Add_ClientActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_Add_ClientActionPerformed

        // add a new client

        // get the client data
        String name = jTextField_Name.getText();
        String phone = jTextField_Phone.getText();
        String username = jTextField_Username.getText();
        //String password = jTextField_Password.getText();

        Prop_User user = new Prop_User();

        // before inserting a new client we need to check if the required data are empty
        // required data -> first name, last name, phone, and address

        if( name.trim().equals("") || phone.trim().equals("") || username.trim().equals("") )
        {
            JOptionPane.showMessageDialog(null, "Enter The Required User Information", "Add User", 2);
        }
        else
        {
            if(user.addNewUser(new Prop_User(0, name, phone, username )))
            {
                JOptionPane.showMessageDialog(null, "New User Added To The System", "Add User", 1);
            }
            else{
                JOptionPane.showMessageDialog(null, "User NOT Added To The System", "Add User", 2);
            }
        }

    }//GEN-LAST:event_jButton_Add_ClientActionPerformed

    private void jButton_Edit_ClientActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_Edit_ClientActionPerformed

        // edit the client data
        // get the client data

        String name = jTextField_Name.getText();
        String phone = jTextField_Phone.getText();
        String username = jTextField_Username.getText();
        //String password = jTextField_Password.getText();

        Prop_User user = new Prop_User();

        try{

            int userId = Integer.valueOf(jTextField_Id.getText());

            // before editing the client data we need to check if the required data are empty
            // required data -> first name, last name, phone, and address

            if( name.trim().equals("") || phone.trim().equals("") || username.trim().equals("") )
            {
                JOptionPane.showMessageDialog(null, "Enter The Required User Information", "Add User", 2);
            }
            else
            {
                if(user.editUserData(new Prop_User(userId, name, phone, username/*, password*/)))
                {
                    JOptionPane.showMessageDialog(null, "User Data Edited", "Edit User", 1);
                }
                else{
                    JOptionPane.showMessageDialog(null, "User Data NOT Edited", "Edit User", 2);
                }
            }
        }catch(Exception ex)
        {
            JOptionPane.showMessageDialog(null, " Enter a Valid User ID ", "Invalid Id", 0);
        }

    }//GEN-LAST:event_jButton_Edit_ClientActionPerformed

    private void jButton_Remove_ClientActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_Remove_ClientActionPerformed

        try{

            // deleting the selected client from the system
            int userId = Integer.valueOf(jTextField_Id.getText());

        Prop_User user = new Prop_User();

            if(jTextField_Id.getText().trim().equals(""))
            {
                JOptionPane.showMessageDialog(null, "Enter the User ID", "Empty ID", 2);
            }
            else{

                // show a confirmation message before deleting the type
                int yes_or_no = JOptionPane.showConfirmDialog(null, "Do You Want To Delete This User", "Delete User", JOptionPane.YES_NO_OPTION);

                if(yes_or_no == JOptionPane.YES_OPTION)
                {
                    if(user.deleteUser(userId))
                    {
                        JOptionPane.showMessageDialog(null, "User Data Deleted", "User Deleted", 1);

                        // clear text fields after the delete
                        jTextField_Id.setText("");
                        jTextField_Name.setText("");
                        jTextField_Username.setText("");
                        jTextField_Phone.setText("");
                        //jTextField_Password.setText("");

                    }else{
                        JOptionPane.showMessageDialog(null, "Operation Failed", "Delete User", 2);
                    }
                }

            }

        }catch(Exception ex)
        {
            JOptionPane.showMessageDialog(null, ex.getMessage()+ " Enter a Valid User ID ", "Invalid Id", 0);
        }

    }//GEN-LAST:event_jButton_Remove_ClientActionPerformed

