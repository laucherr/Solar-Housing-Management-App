    public void fillJtableWithUsersData()
    {
        Prop_User user = new Prop_User();
        ArrayList<Prop_User> userList = user.usersList();
        
        // the jtable columns
        String[] colNames = {"ID","Name", "Phone","Username", ""};
        
        // the jtable row
        // userList.size() = the size of the arraylist
        // 5 = the number of columns
        Object[][] rows = new Object[userList.size()][4];
        
        // add data form the list to the rows
        for(int i = 0; i < userList.size(); i++)
        {
            rows[i][0] = userList.get(i).getId();
            rows[i][1] = userList.get(i).getName();
            rows[i][2] = userList.get(i).getPhone();
            rows[i][3] = userList.get(i).getUsername();
            //rows[i][4] = userList.get(i).getPassword();

        }
        
        DefaultTableModel model = new DefaultTableModel(rows, colNames);
        jTable1.setModel(model);
        
        // change the jtable row height
        jTable1.setRowHeight(40);
        // change the jtable selection background
        jTable1.setSelectionBackground(new Color(27,150,77));
        // change the "ID" column width
        jTable1.getColumnModel().getColumn(0).setPreferredWidth(25);
        // change the "Address" column width
        jTable1.getColumnModel().getColumn(4).setPreferredWidth(150);
        
    }

